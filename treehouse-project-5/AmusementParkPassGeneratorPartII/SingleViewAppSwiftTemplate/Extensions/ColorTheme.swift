import UIKit

extension UIColor {
  struct Theme {
    static var white: UIColor { return .white }
    static var gray: UIColor { return UIColor(red: 214/255, green: 208/255, blue: 217/255, alpha: 1.0) }
    static var green: UIColor { return UIColor(red: 88/255, green: 149/255, blue: 143/255, alpha: 1.0) }
  }
}
